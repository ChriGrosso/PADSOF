package vuelos;

/**
 * Enumeración que define la periodicidad de un vuelo.
 *
 * Tipos de periodicidad:
 * NO_PERIODICO: El vuelo no sigue un patrón regular.
 * DIARIO: El vuelo se realiza todos los días.
 * DIAS_ALTERNOS: El vuelo se realiza en días alternos.
 * 
 * @autor Sofía Castro - sofiai.castro@estudiante.uam.es
 */
public enum Periodicidad {
	NO_PERIODICO, 
	DIARIO, 
	DIAS_ALTERNOS;
}
